독 = [{
 "이름" : "루비독",
 "나이" : "95년생",
 "점프력" : "3",
 "몸무게" : "4",
}, {
 "이름" : "피치독",
 "나이" : "95년생",
 "점프력" : "3",
 "몸무게" : "3",
}, {
 "이름" : "씨독",
 "나이" : "72년생",
 "점프력" : "2",
 "몸무게" : "1",
}, {
 "이름" : "코블독",
 "나이" : "59년생",
 "점프력" : "1",
 "몸무게" : "1",
}]

def way(돌의내구도, 독):
    answer = [i['이름'] for i in 독]
    for i in 독:
        독의위치 = 0
        while 독의위치 < len(돌의내구도)-1:
            독의위치 += int(i['점프력'])
            돌의내구도[독의위치-1] -= int(i['몸무게'])
            if 돌의내구도[독의위치-1] < 0:
                answer[answer.index(i['이름'])] = 'fail'
                break
    return answer

print(way(돌의내구도,독))

